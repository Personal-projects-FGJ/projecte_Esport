    private void carregarTemporades() {
        try {
            List<Temporada> lTemp = bd.getTemporades(); 

            cbTemporada.removeAllItems();

            for (Temporada t : lTemp) {
                cbTemporada.addItem(String.valueOf(t.getAny()));
            }
        } catch (GestorBDEsportException e) {
            System.out.println("Error al carregar les temporades: " + e.getMessage());
        }
    }
    
    
    private void carregarCategories() {
        try {
            List<Categoria> lCat = bd.getCategories(); 

            cbCategoria.removeAllItems();

            for (Categoria c : lCat) {
                cbCategoria.addItem(c.getNom());
            }
        } catch (GestorBDEsportException e) {
            System.out.println("Error al carregar les categories: " + e.getMessage());
        }
    }

    private void btAfegirEquipActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAfegirEquipActionPerformed
        String nom = tfNom.getText();  
        String tipusString = (String) cbTipus.getSelectedItem();
        char tipus = tipusString.charAt(0); 
        int temporada = Integer.parseInt((String) cbTemporada.getSelectedItem()); 
        String categoriaNom = (String) cbCategoria.getSelectedItem(); 
        
        temporadaParam = String.valueOf(temporada);
        
        try {  
            int categoria = bd.getIdCategoriaByNom(categoriaNom);
            
            if (idEquipEditar == null) {
                try {
                    Equip equip = new Equip(nom, tipus, temporada, categoria);
                    bd.addEquip(equip);
                    System.out.println("Equip afegit: " + equip);
                    lbInfo.setForeground(Color.BLUE);
                    lbInfo.setText("Equip afegit.");
                } catch (GestorBDEsportException e) {
                    System.out.println("Error en insertar l'equip: " + e.getMessage());
                    lbInfo.setForeground(Color.RED);
                    lbInfo.setText("Error en inserir l'equip.\n\nMotiu:\n\n" + e.getMessage());
                }
            } else {
                try {
                    Equip equip = new Equip(nom, tipus, temporada, categoria);
                    bd.editEquip(idEquipEditar, equip);
                    System.out.println("Equip editat."+equip);
                    lbInfo.setForeground(Color.BLUE);
                    lbInfo.setText("Equip editat: ");
                } catch (GestorBDEsportException e) {
                    System.out.println("Error en editar l'equip: " + e.getMessage());
                    lbInfo.setForeground(Color.RED);
                    lbInfo.setText("Error en editar l'equip.\n\nMotiu:\n\n" + e.getMessage());
                }
            }

        } catch (GestorBDEsportException e) {
            System.out.println("Error en l'operaci√≥ de l'equip: " + e.getMessage());
            lbInfo.setForeground(Color.RED);
            lbInfo.setText("Error en l'operaci√≥ de l'equip.\n\nMotiu:\n\n" + e.getMessage());
        }
    }//GEN-LAST:event_btAfegirEquipActionPerformed

