    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        char[] password = jPasswordField1.getPassword();
        String passwordText = new String(password);
        String username = tfUsername.getText();
        Usuari u = new Usuari(username, passwordText);
        if (tfUsername.getText().length() > 0 && passwordText.length() > 0) {
            try {
                
                String hashedPass = u.sha1Hash(passwordText);
                hashedPass = hashedPass.toUpperCase();
                //System.out.println(hashedPass);
                int bLogin = bd.loginUser(username, hashedPass);
                if (bLogin == 1) {
                    App app = new App(this.bd);
                    app.setVisible(true);
                    this.dispose();
                } else {
                    lbInfo.setForeground(Color.RED);
                    lbInfo.setText("Usuari o contrasenya incorrectes.");
                }
            } catch (GestorBDEsportException ex) {
                lbInfo.setForeground(Color.RED);
                lbInfo.setText("Error en fer login.\n\nMotiu:\n\n" + ex.getMessage());
            }          
        } else {
            lbInfo.setForeground(Color.RED);
            lbInfo.setText("Informa nom d'usuari i contrasenya. ");
        }
    }//GEN-LAST:event_btEntrarActionPerformed

