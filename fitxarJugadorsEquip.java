    private void btDelJugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDelJugActionPerformed
        int selectedRow = sgInscrits.getSelectedRow();
        if (selectedRow == -1) {
            lbInfo.setForeground(Color.RED);
            lbInfo.setText("Selecciona un jugador a eliminar.");
            return;
        }

        int idJugador = (int) sgInscrits.getValueAt(selectedRow, 0);

        try {
            bd.delJugadorEquip(idJugador, idEquip);
            carregarJugadorsEquip();
            carregarJugadorsDisponibles();
            lbInfo.setForeground(Color.BLUE);
            lbInfo.setText("Jugador eliminat.");
            
        } catch (GestorBDEsportException ex) {
            lbInfo.setForeground(Color.RED);
            lbInfo.setText(ex.getMessage());
        }
    }//GEN-LAST:event_btDelJugActionPerformed

    private void btAddJugActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddJugActionPerformed
        int selectedRow = sgDisponibles.getSelectedRow();
    
        if (selectedRow != -1) {
            pnTitular.setVisible(true);
            setComponentsVisible(false);
            btSiTitular.requestFocus();
        } else {
            lbInfo.setForeground(Color.RED);
            lbInfo.setText( "Selecciona un jugador a afegir. ");
        }
    }//GEN-LAST:event_btAddJugActionPerformed

    private void btSiTitularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSiTitularActionPerformed
       pnTitular.setVisible(false);  
       setComponentsVisible(true);   
        try {
            afegirJugadorAlEquip(true);
            carregarJugadorsEquip();
            carregarJugadorsDisponibles();
        } catch (GestorBDEsportException ex) {
            lbInfo.setForeground(Color.RED);
            lbInfo.setText(ex.getMessage());
        }
    }//GEN-LAST:event_btSiTitularActionPerformed

    private void btNoTitularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNoTitularActionPerformed
       pnTitular.setVisible(false);  
       setComponentsVisible(true); 
       try {
            afegirJugadorAlEquip(false);
            carregarJugadorsEquip();
            carregarJugadorsDisponibles();
            lbInfo.setForeground(Color.BLUE);
            lbInfo.setText( "Jugador afegit al equip. ");
        } catch (GestorBDEsportException ex) {
            lbInfo.setForeground(Color.RED);
            lbInfo.setText(ex.getMessage());
        }
    }//GEN-LAST:event_btNoTitularActionPerformed


