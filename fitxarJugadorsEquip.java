private void carregarJugadorsEquip() throws GestorBDEsportException {
    List<Jugador> jugadors = bd.getJugadorsEquip(idEquip);

    DefaultTableModel model = (DefaultTableModel) sgInscrits.getModel();
    
    model.setRowCount(0);

    for (Jugador j : jugadors) {
        Integer id = j.getId();
        String nomCognoms = j.getNom() + " " + j.getCognoms();
        String nif = j.getIdlegal();
        int esTitular = bd.verificaTitular(idEquip, j.getId());
        Boolean bTitular = null;
        if (esTitular == 1) {
            bTitular = true;
        } else { 
            bTitular = false;
        }
        model.addRow(new Object[]{id, nif, nomCognoms, bTitular});
    }
}

private void carregarJugadorsDisponibles() throws GestorBDEsportException {
    List<Jugador> jugadors = bd.getJugadorsEquipDisponibles(idEquip);

    DefaultTableModel model = (DefaultTableModel) sgDisponibles.getModel();
    
    model.setRowCount(0);

    for (Jugador j : jugadors) {
        Integer id = j.getId();
        String nomCognoms = j.getNom() + " " + j.getCognoms();
        String nif = j.getIdlegal();
        Integer any_revisio = j.getAny_fi_revisio_medica();
        model.addRow(new Object[]{id,nif, nomCognoms, any_revisio});
    }
}
