private void afegirJugadorAlEquip(boolean titular) throws GestorBDEsportException {
    int filaSeleccionada = sgDisponibles.getSelectedRow();
    if (filaSeleccionada != -1) {
        int idJugador = Integer.parseInt(sgDisponibles.getValueAt(filaSeleccionada, 0).toString());
        bd.addJugadorEquip(idJugador, idEquip, titular ? 1 : 0);
    }
}




